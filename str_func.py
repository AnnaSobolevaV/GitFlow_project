def str_func(string_value):
    """Принимает на вход строку
    и возвращает ее со всеми заглавными буквами """
    return string_value.upper()

def str_to_title_words(string_value):
    """делает заглавными первые буквы каждого слова в строке,
     поступившей на вход функции"""
    return string_value.title()